    private void sendBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendBtnActionPerformed
            
        car.setType(typeTxtFld.getText());
        car.setTransmission(transTxtFld.getText());
        
        
        if(car.getType().equals("0"))
        {
            if(car.getTransmission().equals("Manual") || car.getTransmission().equals("Auto"))
            {
                 option1Lbl.setText("0 -> 2023 Toyota Vios 1.5E AT");
                 option2Lbl.setText("1 -> 2019 Proton Persona Executive 1.6");
                 option3Lbl.setText("2 -> Honda CR-V 2.0");
            }
            
        }
        else if(car.getType().equals("1"))
        {
             if(car.getTransmission().equals("Manual") || car.getTransmission().equals("Auto"))
            {
               option1Lbl.setText("0 -> 2022 Perodua Alza 1.5 X");
               option2Lbl.setText("1 -> 2019 Toyota Avanza 1.5E");
               option3Lbl.setText("2 -> 2023 Toyota Vellfire 2.5"); 
            }
        
        }
        else if(car.getType().equals("2"))
        {
             if(car.getTransmission().equals("Manual") || car.getTransmission().equals("Auto"))
            {
               option1Lbl.setText("0 -> Aston Martin Vanquish");
               option2Lbl.setText("1 -> 2021 Porsche Taycan RWD ");
               option3Lbl.setText("2 -> 2022 Ferrari 296 GTB V6"); 
            }
            
        }
             
        if(car.getType() != null){
        
            submtBtn.setEnabled(true);
            ResBtn.setEnabled(true);
            sendBtn.setEnabled(false);
        
        }
       
    }//GEN-LAST:event_sendBtnActionPerformed

