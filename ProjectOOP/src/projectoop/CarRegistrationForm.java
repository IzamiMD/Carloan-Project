    private void submtBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submtBtnActionPerformed
        
        
        
       


        if(typeTxtFld.getText().equals("0"))
        {   
            car = new Sedan();
            car.setType("Sedan");

            if(modelTxtFld.getText().equalsIgnoreCase("0")) ((Sedan)car).setModel((modelTxtFld.getText()));
            else if(modelTxtFld.getText().equalsIgnoreCase("1")) ((Sedan)car).setModel((modelTxtFld.getText()));
            else if(modelTxtFld.getText().equalsIgnoreCase("2")) ((Sedan)car).setModel((modelTxtFld.getText()));
            else JOptionPane.showMessageDialog(null, "Wrong input, please try again");
            
            if(transTxtFld.getText().equalsIgnoreCase("0")) ((Sedan)car).setTransmission((transTxtFld.getText()));
            else if(transTxtFld.getText().equalsIgnoreCase("1")) ((Sedan)car).setTransmission((transTxtFld.getText()));

            price = ((Sedan)car).calculatePrice();
            model = ((Sedan)car).getModel(Integer.parseInt(modelTxtFld.getText()));
        }
        else if(typeTxtFld.getText().equals("1"))
        {
            car = new SuperCar();
            car.setType("SuperCar");

            if(modelTxtFld.getText().equalsIgnoreCase("0")) ((SuperCar)car).setModel((modelTxtFld.getText()));
            else if(modelTxtFld.getText().equalsIgnoreCase("1")) ((SuperCar)car).setModel((modelTxtFld.getText()));
            else if(modelTxtFld.getText().equalsIgnoreCase("2")) ((SuperCar)car).setModel((modelTxtFld.getText()));
            else JOptionPane.showMessageDialog(null, "Wrong input, please try again");
            
            if(transTxtFld.getText().equalsIgnoreCase("0")) ((SuperCar)car).setTransmission((transTxtFld.getText()));
            else if(transTxtFld.getText().equalsIgnoreCase("1")) ((SuperCar)car).setTransmission((transTxtFld.getText()));

            price = ((Sedan)car).calculatePrice();
            model = ((SuperCar)car).getModel(Integer.parseInt(modelTxtFld.getText()));
        }
        else if(typeTxtFld.getText().equals("2"))
        {
            car = new MPV();
            car.setType("MPV");

            if(modelTxtFld.getText().equalsIgnoreCase("0")) ((MPV)car).setModel((modelTxtFld.getText()));
            else if(modelTxtFld.getText().equalsIgnoreCase("1")) ((MPV)car).setModel((modelTxtFld.getText()));
            else if(modelTxtFld.getText().equalsIgnoreCase("2")) ((MPV)car).setModel((modelTxtFld.getText()));
            else JOptionPane.showMessageDialog(null, "Wrong input, please try again");
            
            if(transTxtFld.getText().equalsIgnoreCase("0")) ((MPV)car).setTransmission((transTxtFld.getText()));
            else if(transTxtFld.getText().equalsIgnoreCase("1")) ((MPV)car).setTransmission((transTxtFld.getText()));
            
            price = ((Sedan)car).calculatePrice();
            model = ((MPV)car).getModel(Integer.parseInt(modelTxtFld.getText()));
            
        }

        String insertQuery = "INSERT INTO car VALUES (?,?,?,?,?,?,?,?)";
        
        try{
            dbCtrl.pstmt = dbCtrl.myConn.prepareStatement(insertQuery);
            dbCtrl.pstmt.setInt(1, 1);
            dbCtrl.pstmt.setString(2, car.getType());
            dbCtrl.pstmt.setString(3, car.getTransmission());
            dbCtrl.pstmt.setString(4, model);
            dbCtrl.pstmt.setDouble(5, price);
            dbCtrl.pstmt.setDouble(6, Double.parseDouble(loanTxtFld.getText()));
            dbCtrl.pstmt.setDouble(7,  Double.parseDouble(downPayTxtFld.getText()));
            dbCtrl.pstmt.setString(8,bankTxtFld.getText());

            dbCtrl.pstmt.executeUpdate();
            

            JOptionPane.showMessageDialog(null, "Your car info is successfully submitted");

            clearInputForm();
            

            }
        
        catch(NumberFormatException e){
            System.out.println("Error: " + e.getMessage());
//            speedTxtFld.setText("");
//            speedTxtFld.requestFocus();
        
        }
        
        catch(SQLException e){
            System.out.println("Error: " + e.getMessage());
        }
        
        catch(ClassCastException c)
        {
            System.out.println("Error: " + c.getMessage());
        }
        
         
        if(car.getType() != null){
        
            submtBtn.setEnabled(false);
            ResBtn.setEnabled(true);
            sendBtn.setEnabled(false);

        }
        
        
        //        customer.setLoanDuration(Integer.parseInt(loanTxtFld.getText()));
//        customer.setDownPayment(Integer.parseInt(downPayTxtFld.getText()));
//        bank.setBankName(bankTxtFld.getText());
//        customer.setPayment(Integer.parseInt(payTxtFld.getText()));
        

     
    }//GEN-LAST:event_submtBtnActionPerformed

